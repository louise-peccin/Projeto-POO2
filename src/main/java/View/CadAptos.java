    private void carregarApartamentos() {
        try {
        ApartamentoDAO aptoDAO = new ApartamentoDAO();
        List<Apartamento> apartamentos = aptoDAO.buscarPorEmail(emailUsuarioLogado);
        
        tableModel.setRowCount(0); // Limpa a tabela
        
        for (Apartamento apto : apartamentos) {
            tableModel.addRow(new Object[]{
                apto.getId(),
                apto.getCidade(),
                apto.getBairro(),
                apto.getQuartos(),
                apto.getHospedesMax(),
                apto.getPreco(),
                apto.getDescricao()
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar apartamentos: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }
    
    private void limparCampos() {
        ctCidadeApto.setText("");
        ctBairroApto.setText("");
        cbQuatosApto.setSelectedIndex(0);
        cbHospedesApto.setSelectedIndex(0);
        ctPrecoApto.setText("");
        taDescricaoApto.setText("");
    }
