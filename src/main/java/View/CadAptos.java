    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         try {
            String cidade = ctCidadeApto.getText();
            String bairro = ctBairroApto.getText();
            int quartos = Integer.parseInt(cbQuatosApto.getSelectedItem().toString());
            int hospedesMax = Integer.parseInt(cbHospedesApto.getSelectedItem().toString());
            double preco = Double.parseDouble(ctPrecoApto.getText());
            String descricao = taDescricaoApto.getText();

            Apartamento apto = new Apartamento(cidade, bairro, quartos, hospedesMax, preco, descricao);

            ApartamentoDAO aptoDAO = new ApartamentoDAO();
            aptoDAO.inserir(apto, emailUsuarioLogado);

            // Limpar campos após salvar
            limparCampos();

            // Recarregar a lista de apartamentos na tabela
            carregarApartamentos();

            JOptionPane.showMessageDialog(this, "Apartamento salvo com sucesso!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um valor numérico válido para o preço.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao salvar o apartamento: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

