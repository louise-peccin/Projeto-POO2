    private void btnApagarAptoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarAptoActionPerformed
        int selectedRow = TabelaAptosProp.getSelectedRow();
        if (selectedRow >= 0) {
            int aptoId = (int) tableModel.getValueAt(selectedRow, 0);

            int confirm = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja apagar este apartamento?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                ApartamentoDAO dao = new ApartamentoDAO();
                dao.excluir(aptoId);
                JOptionPane.showMessageDialog(this, "Apartamento apagado com sucesso!");
                carregarApartamentos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um apartamento para apagar.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnApagarAptoActionPerformed

